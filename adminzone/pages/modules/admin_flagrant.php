		$this->edit_this_label=do_lang_tempcode('EDIT_THIS_FLAGRANT');
		$this->edit_one_label=do_lang_tempcode('EDIT_FLAGRANT');

		if ($type=='misc') return $this->misc();

		return new ocp_tempcode();
	}

