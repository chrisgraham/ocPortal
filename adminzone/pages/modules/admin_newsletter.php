			$week_days=array(1=>do_lang('MONDAY'),2=>do_lang('TUESDAY'),3=>do_lang('WEDNESDAY'),4=>do_lang('THURSDAY'),5=>do_lang('FRIDAY'),6=>do_lang('SATURDAY'),7=>do_lang('SUNDAY'));
			foreach ($week_days as $i=>$this_day)
