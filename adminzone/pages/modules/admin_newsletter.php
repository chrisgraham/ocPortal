	{
		$title=get_screen_title('VIEW_NEWSLETTER_SUBSCRIBERS');

		$lang=choose_language($title);
		if (is_object($lang)) return $lang;

		$id=either_param('id',NULL);
		$level=get_param_integer('level',NULL);

