				$GLOBALS['SITE_DB']->query_delete('shopping_cart',array('product_id'=>$id));
				$this->delete_actualisation($_id);
			}

			unset($_GET['redirect']);
		}

		$this->donext_category_id=$category_id;
		$this->donext_catalogue_name=$catalogue_name;
	}

