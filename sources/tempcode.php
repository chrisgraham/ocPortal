		}

		//$tempcode_profile_log_end=microtime();
		//tempcode_profile_log_diff($tempcode_profile_log_start,$tempcode_profile_log_end,$this->seq_parts);

		if (($MEMORY_OVER_SPEED) || ($NO_EVAL_CACHE))
