	if ($result[1]!='') $SEO_DESCRIPTION=$result[1];
	if ($title!==NULL) $SEO_TITLE=str_replace('&ndash;','-',str_replace('&copy;','(c)',str_replace('&#039;','\'',$title)));
}

/**
