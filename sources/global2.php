 *
 * @return integer		A random seed
 */
function make_seed()
{
	list($usec,$sec)=explode(' ',microtime(false));
	$u=produce_salt();
	return intval(floatval($sec)*floatval($usec))+ord(substr($u,0,1))+(ord(substr($u,1,2))<<8);
}

