{
	static $minify=NULL;
	if ($_minify!==NULL) $minify=$_minify;
	static $https=NULL;
	if ($_https!==NULL) $https=$_https;
	static $mobile=NULL;
	if ($_mobile!==NULL) $mobile=$_mobile;

