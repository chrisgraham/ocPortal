	require_code('seo2');
	seo_meta_set_for_explicit('image',strval($id),$meta_keywords,$meta_description);

	decache('main_download_category');
	decache('main_image_fader');

	require_lang('galleries');
	require_code('feedback');
	update_spacer_post(
		$allow_comments!=0,
		'images',
