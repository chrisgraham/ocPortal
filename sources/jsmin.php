class JSMin
{
	var $a = '';
	var $b = '';
	var $input = '';
	var $inputIndex = 0;
	var $inputLength = 0;
	var $lookAhead = NULL;
	var $output = '';

	// -- Public Instance Methods ------------------------------------------------

	/**
	 * Constructor function
	 *
	 * @param 	string		Javascript to minimise
	 */
	function JSMin($input)
	{
		$this->input = str_replace("\r\n", "\n", $input);
		$this->inputLength = strlen($this->input);
	}

	// -- Protected Instance Methods ---------------------------------------------

	/**
	 * Handle internal parse situation
	 *
	 * @param 	integer		Action type
	 * @return	?array		Error (NULL: no error)
	 */
	function action($d)
	{
		$chr_lf=10;
		$chr_space=32;

		switch($d)
		{
			case 1:
				$this->output .= $this->a;

			case 2:
				$this->a = $this->b;

				if ($this->a === "'" || $this->a === '"')
				{
					while (true)
					{
						$this->output .= $this->a;
						$this->a			 = $this->get();

						if ($this->a === $this->b)
						{
							break;
						}

						if (ord($this->a) <= $chr_lf)
						{
							return array('Unterminated string literal.');
						}

						if ($this->a === '\\')
						{
							$this->output .= $this->a;
							$this->a			 = $this->get();
						}
					}
				}

			case 3:
				$this->b = $this->next();
				if (is_array($this->b)) return $this->b;

				if ($this->b === '/' && (
						$this->a === '(' || $this->a === ',' || $this->a === '=' ||
						$this->a === ':' || $this->a === '[' || $this->a === '!' ||
						$this->a === '&' || $this->a === '|' || $this->a === '?'))
				{

					$this->output .= $this->a . $this->b;

					while (true)
					{
						$this->a = $this->get();

						if ($this->a === '/')
						{
							break;
						} elseif ($this->a === '\\')
						{
							$this->output .= $this->a;
							$this->a			 = $this->get();
						} elseif (ord($this->a) <= $chr_lf)
						{
							return array('Unterminated regular expression literal.');
						}

						$this->output .= $this->a;
					}

					$this->b = $this->next();
					if (is_array($this->b)) return $this->b;
				}
		}

		return NULL;
	}

	/**
	 * Get the next stream token
	 *
	 * @return 	string		Next stream token
	 */
	function get()
	{
		$chr_space=32;

		$c = $this->lookAhead;
		$this->lookAhead = NULL;

		if ($c === NULL)
		{
			if ($this->inputIndex < $this->inputLength)
			{
				$c = $this->input[$this->inputIndex];
				$this->inputIndex += 1;
			} else
			{
				$c = NULL;
			}
		}

		if ($c === "\r")
		{
			return "\n";
		}

		if ($c === NULL || $c === "\n" || ord($c) >= $chr_space)
		{
			return $c;
		}

		return ' ';
	}

	/**
	 * Find whether a character is alphanumeric
	 *
	 * @param 	string		Character
	 * @return 	boolean		Whether it is
	 */
	function isAlphaNum($c)
	{
		if ($c===NULL) return false;
		return ord($c) > 126 || $c == '\\' || preg_match('/^[\w\$]$/', $c) == 1;
	}

	/**
	 * Do minification process
	 *
	 * @return 	string		Minified Javascript
	 */
	function min()
	{
		$this->a = "\n";
		$test = $this->action(3);
		if (is_array($test)) return $this->input; // Error

		while ($this->a !== NULL)
		{
			switch ($this->a)
			{
				case ' ':
					if ($this->isAlphaNum($this->b))
					{
						$test = $this->action(1);
						if (is_array($test)) return $this->input; // Error
					} else
					{
						$test = $this->action(2);
						if (is_array($test)) return $this->input; // Error
					}
					break;

				case "\n":
					switch ($this->b)
					{
						case '{':
						case '[':
						case '(':
						case '+':
						case '-':
							$test = $this->action(1);
							if (is_array($test)) return $this->input; // Error
							break;

						case ' ':
							$test = $this->action(3);
							if (is_array($test)) return $this->input; // Error
							break;

						default:
							if ($this->isAlphaNum($this->b))
							{
								$test = $this->action(1);
								if (is_array($test)) return $this->input; // Error
							}
							else
							{
								$test = $this->action(2);
								if (is_array($test)) return $this->input; // Error
							}
					}
					break;

				default:
					switch ($this->b)
					{
						case ' ':
							if ($this->isAlphaNum($this->a))
							{
								$test = $this->action(1);
								if (is_array($test)) return $this->input; // Error
								break;
							}

							$test = $this->action(3);
							if (is_array($test)) return $this->input; // Error
							break;

						case "\n":
							switch ($this->a)
							{
								case '}':
								case ']':
								case ')':
								case '+':
								case '-':
								case '"':
								case "'":
									$test = $this->action(1);
									if (is_array($test)) return $this->input; // Error
									break;

								default:
									if ($this->isAlphaNum($this->a))
									{
										$test = $this->action(1);
										if (is_array($test)) return $this->input; // Error
									}
									else
									{
										$test = $this->action(3);
										if (is_array($test)) return $this->input; // Error
									}
							}
							break;

						default:
							$test = $this->action(1);
							if (is_array($test)) return $this->input; // Error
							break;
					}
			}
		}

		return $this->output;
	}

	/**
	 * Get the next item in the stream
	 *
	 * @return 	mixed			Next item or error (array)
	 */
	function next()
	{
		$chr_lf=10;

		$c = $this->get();

		if ($c === '/')
		{
			switch($this->peek())
			{
				case '/':
					while (true)
					{
						$c = $this->get();

						if (ord($c) <= $chr_lf)
						{
							return $c;
						}
					}

				case '*':
					$this->get();

					while (true)
					{
						switch($this->get())
						{
							case '*':
								if ($this->peek() === '/')
								{
									$this->get();
									return ' ';
								}
								break;

							case NULL:
								return array('Unterminated comment.');
						}
					}

				default:
					return $c;
			}
		}

		return $c;
	}

	/**
	 * Find what's next in the stream
	 *
	 * @return 	string		What's next
	 */
	function peek()
	{
		$this->lookAhead = $this->get();
		return $this->lookAhead;
	}
}
