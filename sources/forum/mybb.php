		$i=0;
		$firsttime=array();
		$username=array();
		$memberid=array();
		$datetimes=array();
		$rs=array();
		while (array_key_exists($i,$rows))
		{
			$r=$rows[$i];

			$id=$r['tid'];

			$r['topic_time']=$r['dateline'];
			$r['topic_poster']=$r['uid'];
			$r['last_poster']=$r['lastposteruid'];
			$r['last_time']=$r['lastpost'];


			$firsttime[$id]=$r['dateline'];

			$post_rows=$this->connection->query('SELECT * FROM '.$this->connection->get_table_prefix().'posts p WHERE tid='.strval((integer)$id).' AND message NOT LIKE \''.db_encode_like(substr(do_lang('SPACER_POST','','','',get_site_default_lang()),0,20).'%').'\' ORDER BY dateline DESC',1);

			if (!array_key_exists(0,$post_rows))
			{
				$i++;
				continue;
			}
			$r2=$post_rows[0];

			$username[$id]=$r2['username'];
			$username[$id]=$r2['uid'];
			$datetimes[$id]=$r2['dateline'];
			$rs[$id]=$r;

			$i++;
		}
		if ($i>0)
		{
			arsort($datetimes);
			$i=0;
			$out=array();
			if (count($datetimes)>0)
			{
				foreach ($datetimes as $id=>$datetime)
