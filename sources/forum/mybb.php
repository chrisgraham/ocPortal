		ocp_setcookie('mybbuser',$loguid.'_'.$loginkey);

		$current_ip=get_ip_address();

		$session_row=$this->connection->query('SELECT * FROM '.$this->connection->get_table_prefix().'sessions WHERE '.db_string_equal_to('ip',$current_ip),1);
		$session_row=(!empty($session_row[0]))?$session_row[0]:array();
		$session_id=(!empty($session_row['sid']))?$session_row['sid']:'';

		if (!empty($session_id))
		{
			$this->connection->query('UPDATE '.$this->connection->get_table_prefix().'sessions SET time='.strval(time()).', uid='.strval($loguid).' WHERE '.db_string_equal_to('sid',$session_id),1);
		} else
		{
			$session_id=md5(strval(time()));
			$this->connection->query_insert('sessions', array('sid'=>$session_id,'uid'=>$id,'time'=>time(),'ip'=>$current_ip));
		}

		//Now lets try and set a COOKIE of MyBB Session ID
		@ocp_setcookie('sid', $session_id);

		$_COOKIE['mybbuser']=strval($loguid).'_'.$loginkey;
		$_COOKIE['sid']=$session_id;
	}

	/**
	 * Find if the given member id and password is valid. If username is NULL, then the member id is used instead.
	 * All authorisation, cookies, and form-logins, are passed through this function.
	 * Some forums do cookie logins differently, so a Boolean is passed in to indicate whether it is a cookie login.
	 *
	 * @param  ?SHORT_TEXT	The member username (NULL: don't use this in the authentication - but look it up using the ID if needed)
	 * @param  MEMBER			The member id
	 * @param  MD5				The md5-hashed password
	 * @param  string			The raw password
	 * @param  boolean		Whether this is a cookie login
	 * @return array			A map of 'id' and 'error'. If 'id' is NULL, an error occurred and 'error' is set
	 */
	function forum_authorise_login($username,$userid,$password_hashed,$password_raw,$cookie_login=false)
	{
		global $SITE_INFO;

		$out=array();
		$out['id']=NULL;

		if (is_null($userid))
		{
			$rows=$this->connection->query_select('users',array('*'),array('username'=>$username),'',1);
			if (array_key_exists(0,$rows))
			{
				$this->MEMBER_ROWS_CACHED[$rows[0]['uid']]=$rows[0];
			}
		} else
		{
			$rows=array();
			$rows[0]=$this->get_member_row($userid);
		}

		if (!array_key_exists(0,$rows) || $rows[0]==null) // All hands to lifeboats
		{
			$out['error']=(do_lang_tempcode('_USER_NO_EXIST',$username));
			return $out;
		}
		$row=$rows[0];
		if ($this->is_banned($row['uid'])) // All hands to the guns
		{
			$out['error']=(do_lang_tempcode('USER_BANNED'));
			return $out;
		}

		if ($cookie_login)
		{
			$cookie_loginkey='';
			$cookie_member=NULL;
			if (!empty($_COOKIE['mybbuser']))
			{
				$cookie_info=array();
				$cookie_info=explode('_',$_COOKIE['mybbuser']);
				$cookie_member=(!empty($cookie_info[0]))?intval($cookie_info[0]):NULL;
				$cookie_loginkey=(!empty($cookie_info[1]))?$cookie_info[1]:'';
			}

			$lookup=$this->connection->query_value_null_ok('users','uid',array('loginkey'=>$cookie_loginkey,'uid'=>$cookie_member));
			if ($row['uid']!==$lookup)
			{
				$out['error']=(do_lang_tempcode('USER_BAD_PASSWORD'));
				return $out;
			}
		} else
		{
			if($this->salt_password($password_hashed, $row['salt'])!=$row['password'])
			{
				$out['error']=(do_lang_tempcode('USER_BAD_PASSWORD'));
				return $out;
			}
		}

		$out['id']=$row['uid'];
		return $out;
	}

	/**
	 * Salts a password based on a supplied salt.
	 *
	 * @param  string			The md5()'ed password.
	 * @param  string			The salt.
	 * @return string			The password hash.
	 */
	function salt_password($password, $salt)
