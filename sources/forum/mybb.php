					$out[$i]['firsttitle']=$fp_rows[0]['subject'];
					if ($show_first_posts)
					{
						global $LAX_COMCODE;
						$temp=$LAX_COMCODE;
						$LAX_COMCODE=true;
						$out[$i]['firstpost']=$fp_rows[0]['message'];
						$LAX_COMCODE=$temp;
					}

					$i++;
					if ($i==$limit) break;
				}
			}

			return $out;
		}
		return NULL;
	}

