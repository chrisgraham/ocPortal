	function forum_create_cookie($id,$name,$password)
	{
		global $SITE_INFO;

		unset($name);
		unset($password);

		$row=$this->get_member_row($id);
		$loginkey=$row['loginkey']; //used for 'mybbuser' memberid.'_'.'loginkey'
		$loguid=$row['uid']; //member id

