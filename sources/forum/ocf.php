		$avatar=$this->get_member_row_field($member,'m_avatar_url');
		if (is_null($avatar))
		{
			$avatar='';
		} else
		{
			$base_url=get_base_url();
			if (substr($avatar,0,strlen($base_url)+1)==$base_url.'/') // So we can do an is_file check
			{
				$avatar=substr($avatar,strlen($base_url)+1);
