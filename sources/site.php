/**
 * Standard code module initialisation function.
 */
function init__site()
{
	if (defined('BREADCRUMB_CROP_LENGTH')) return;

	global $HELPER_PANEL_TEXT,$HELPER_PANEL_HTML,$HELPER_PANEL_PIC,$HELPER_PANEL_TUTORIAL;
