		require_css('swfupload');

		// Find all sounds available
		$library=array();
		$dh=@opendir(get_file_base().'/data/sounds');
		if ($dh!==false)
		{
			while (($f=readdir($dh))!==false)
			{
				if (substr(strtolower($f),-4)=='.mp3')
				{
					$library[$f]=array(
						'EFFECT_SHORT'=>$f,
						'EFFECT'=>'data/sounds/'.$f,
					);
				}
			}
		}
		$dh=@opendir(get_file_base().'/data_custom/sounds');
		if ($dh!==false)
		{
			while (($f=readdir($dh))!==false)
			{
				if (substr(strtolower($f),-4)=='.mp3')
				{
					$library[$f]=array(
						'EFFECT_SHORT'=>$f,
